    private void thongKeTheoNgay() {
        Date ngay = jthongKeTheoNgay.getDate();
        ArrayList<NhanVien> dsNhanVien = bllNhanVien.nhanVienTraLuong(ngay);
        hienNhanVienLenBang(dsNhanVien);
        jLabel_tongLuongNhanVien.setText(quanLy.tinhTongLuongNhanVien(dsNhanVien) + " VND");
        ArrayList<CacChiPhiKhac> dsChiPhi = bllCacChiPhiKhac.thongKeTheoNgay(ngay);
        hienChiPhiKhacLenBang(dsChiPhi);
        jLabel_tongChiPhiPhatSinh.setText(quanLy.tinhTongCacChiPhiKhac(dsChiPhi) + " VND");

        sanPhamBan = bllBanHang.sanPhamBanNgay(ngay);
        hienThiSanPhamBanLenBang(sanPhamBan);
        jLabel_tongTienChi.setText(quanLy.tinhTongGiaMuaSanPham(sanPhamNhap) + " VND");
        jLabel_tongTienBanSanPham.setText(quanLy.tinhTongGiaBanSanPham(sanPhamBan) + " VND");
        jLabel_doanhThu.setText(quanLy.tinhTongGiaBanSanPham(sanPhamBan) + " VND");
        jTextField1.setText(quanLy.tinhLoiNhuan(dsNhanVien, sanPhamNhap, sanPhamBan, dsChiPhi) + " VND");
    }

    private void hienThiSanPhamLenBang(ArrayList<SanPham> ds) {
        if (ds != null) {
            dtmSanPham.setRowCount(0);
            for (SanPham sp : ds) {
                Vector<String> v = new Vector<>();
                v.add(sp.getTenSanPham());
                if (sp instanceof Sach) {
                    v.add("Sách");
                } else if (sp instanceof DiaNhac) {
                    v.add("Đĩa nhạc");
                } else {
                    v.add("Đĩa phim");
                }
                v.add(sp.getGiaMua() + " VND");
                v.add(sp.getSoLuong() + "");
                dtmSanPham.addRow(v);
            }
            jTable_hangNhapVe.setModel(dtmSanPham);
        }
    }

