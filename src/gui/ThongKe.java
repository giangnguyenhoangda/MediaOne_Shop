    private void thongKeTrongKhoanMotThoiGian() {
        Date ngayMin = jDateChooser_start.getDate();
        Date ngayMax = jDateChooser_end.getDate();
        ArrayList<NhanVien> dsNhanVien = bllNhanVien.nhanVienTraLuong(ngayMin, ngayMax);
        hienNhanVienLenBang(dsNhanVien);
        jLabel_tongLuongNhanVien.setText(quanLy.tinhTongLuongNhanVien(dsNhanVien) + " VND");
        ArrayList<CacChiPhiKhac> dsChiPhi = bllCacChiPhiKhac.thongKeTheoNgay(ngayMin, ngayMax);
        hienChiPhiKhacLenBang(dsChiPhi);
        jLabel_tongChiPhiPhatSinh.setText(quanLy.tinhTongCacChiPhiKhac(dsChiPhi) + " VND");
        ArrayList<Sach> sachNhap = bllSach.sachNhapNgay(ngayMin, ngayMax);
        ArrayList<DiaNhac> diaNhacNhap = bLLDiaNhac.diaNhacNhapNgay(ngayMin, ngayMax);
        ArrayList<DiaPhim> diaPhimNhap = bllDiaPhim.diaPhimNhapNgay(ngayMin, ngayMax);
        sanPhamNhap.clear();
        for (Sach s : sachNhap) {
            sanPhamNhap.add(s);
        }
        for (DiaNhac d : diaNhacNhap) {
            sanPhamNhap.add(d);
        }
        for (DiaPhim d : diaPhimNhap) {
            sanPhamNhap.add(d);
        }
        hienThiSanPhamLenBang(sanPhamNhap);
        sanPhamBan = bllBanHang.sanPhamBanNgay(ngayMin, ngayMax);
        hienThiSanPhamBanLenBang(sanPhamBan);
        jLabel_tongTienChi.setText(quanLy.tinhTongGiaMuaSanPham(sanPhamNhap) + " VND");
        jLabel_tongTienBanSanPham.setText(quanLy.tinhTongGiaBanSanPham(sanPhamBan) + " VND");
        jLabel_doanhThu.setText(quanLy.tinhTongGiaBanSanPham(sanPhamBan) + " VND");
        jTextField1.setText(quanLy.tinhLoiNhuan(dsNhanVien, sanPhamNhap, sanPhamBan, dsChiPhi) + " VND");
    }

