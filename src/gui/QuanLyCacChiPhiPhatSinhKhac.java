    private void lamSach() {
        txtLoaiChiPhi.setText("");
        ngayDung.setDate(null);
        tienDung.setValue(0);
    }

    private void hienThiChiTietLenBang(ArrayList<CacChiPhiKhac> ds) {
        dtm.setRowCount(0);
        if (ds != null) {
            for (CacChiPhiKhac c : ds) {
                Vector<String> v = new Vector<>();
                v.add(c.getLoaiChiPhi());
                v.add(sdf.format(c.getNgay()));
                v.add(c.getTienTra() + " VND");
                dtm.addRow(v);
            }
            tbl.setModel(dtm);
        }
    }

