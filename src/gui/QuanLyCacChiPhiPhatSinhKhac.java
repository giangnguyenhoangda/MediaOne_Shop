    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String loaiChiPhi = txtLoaiChiPhi.getText();
        Date ngay = ngayDung.getDate();
        Integer tien = tienDung.getValue();
        int xacNhan = JOptionPane.showConfirmDialog(null, "Bạn muốn thêm chi phí?", "Xác nhận", JOptionPane.YES_OPTION);
        if (xacNhan == JOptionPane.YES_OPTION) {
            if (bll.themChiPhi(loaiChiPhi, ngay, tien) > 0) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
                hienThiChiTietLenBang(bll.layToanBo());
                lamSach();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int[] rows = tbl.getSelectedRows();
        if (rows.length == 0) {
            JOptionPane.showMessageDialog(null, "Chọn chi phí cần xóa trước.");
        } else {
            int xacNhan = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (xacNhan == JOptionPane.YES_OPTION) {
                try {
                    int cout = 0;
                    for (int i : rows) {
                        String loaiChiPhi = tbl.getValueAt(i, 0) + "";
                        String strNgay = tbl.getValueAt(i, 1) + "";
                        String strTien = tbl.getValueAt(i, 2) + "";
                        strTien = strTien.substring(0, strTien.length() - 4);
                        Date ngay = sdf.parse(strNgay);
                        int tien = Integer.parseInt(strTien);
                        cout += bll.xoaChiPhi(loaiChiPhi, ngay, tien);
                    }
                    if (cout > 0) {
                        JOptionPane.showMessageDialog(null, "Xóa thành công " + cout + "/" + rows.length + " chi phí");
                        lamSach();
                        hienThiChiTietLenBang(bll.layToanBo());
                    } else {
                        JOptionPane.showMessageDialog(null, "Xóa thát bại");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int index = tbl.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Chọn dữ liệu cần sửa trước");
        } else {
            int xacNhan = JOptionPane.showConfirmDialog(null, "Bạn muốn sửa ?", "Xác nhận", JOptionPane.YES_OPTION);
            if (xacNhan == JOptionPane.YES_OPTION) {
                String loaiChiPhiGoc = tbl.getValueAt(index, 0) + "";
                String strNgayGoc = tbl.getValueAt(index, 1) + "";
                String loaiChiPhi = txtLoaiChiPhi.getText();
                Date ngay = ngayDung.getDate();
                Integer tien = tienDung.getValue();
                if (loaiChiPhi.equalsIgnoreCase(loaiChiPhiGoc)) {
                    if (bll.suaChiPhi(loaiChiPhi, ngay, tien) > 0) {
                        JOptionPane.showMessageDialog(null, "Sửa thành công");
                        lamSach();
                        hienThiChiTietLenBang(bll.layToanBo());
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Không thể sửa loại chi phí");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tblMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMousePressed
        // TODO add your handling code here:
        try {
            int i = tbl.getSelectedRow();
            txtLoaiChiPhi.setText(tbl.getValueAt(i, 0) + "");
            String ngay = tbl.getValueAt(i, 1) + "";
            String tien = tbl.getValueAt(i, 2) + "";
            tien = tien.substring(0, tien.length() - 4);
            ngayDung.setDate(sdf.parse(ngay));
            tienDung.setValue(Integer.parseInt(tien));
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_tblMousePressed

